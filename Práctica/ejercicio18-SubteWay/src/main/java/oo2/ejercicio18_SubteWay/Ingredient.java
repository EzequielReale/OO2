package oo2.ejercicio18_SubteWay;

public class Ingredient {

	String name;
	double price;
	
	
	public Ingredient(String name, double price) {
		this.name = name;
		this.price = price;
	}

	public String getName() {
		return name;
	}

	public double getPrice() {
		return price;
	}
}
